#Break_Statement
for i in range(10):
    if i==3:
        break
    print(i)
print("Bye")

#Continue_Statement
for i in range(10):
    if i==3:
        continue
    print(i)
print("Take care")

#Pass_Statement
for i in range(10):
    if i==3:
       pass
    print(i)
print("Good Bye")